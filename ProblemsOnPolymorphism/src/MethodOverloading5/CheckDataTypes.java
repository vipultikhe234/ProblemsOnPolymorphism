package MethodOverloading5;

public class CheckDataTypes {

	public void check(int x) {
		System.out.println("Data Type is int");
	}

	public void check(double x) {
		System.out.println("Data Type is double");
	}

	public void check(long x) {
		System.out.println("Data Type is long");
	}

	public void check(float x) {
		System.out.println("Data Type is float");
	}

}